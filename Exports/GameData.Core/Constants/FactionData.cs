    /// <summary>
    /// A faction template entry with masks and explicit enemy/friend faction IDs.
    /// </summary>
    public readonly record struct FactionTemplate(
        uint FactionId, uint OurMask, uint FriendlyMask, uint HostileMask,
        uint EnemyFaction1, uint EnemyFaction2, uint EnemyFaction3, uint EnemyFaction4,
        uint FriendFaction1, uint FriendFaction2, uint FriendFaction3, uint FriendFaction4);

    /// <summary>
    /// Compute the reaction between two units given their faction template IDs.
    /// Mirrors WoW's GetReactionTo algorithm using mask-based faction comparison.
    /// </summary>
    public static UnitReaction GetReaction(uint playerFactionTemplateId, uint unitFactionTemplateId)
    {
        if (!Templates.TryGetValue(playerFactionTemplateId, out var playerFt) ||
            !Templates.TryGetValue(unitFactionTemplateId, out var unitFt))
            return UnitReaction.Neutral;

        // 1. Check explicit enemy factions (unit lists player's faction as enemy)
        if (IsEnemyFaction(unitFt, playerFt.FactionId))
            return UnitReaction.Hostile;

        // 2. Check explicit friend factions (unit lists player's faction as friend)
        if (IsFriendFaction(unitFt, playerFt.FactionId))
            return UnitReaction.Friendly;

        // 3. Check explicit enemy factions (player lists unit's faction as enemy)
        if (IsEnemyFaction(playerFt, unitFt.FactionId))
            return UnitReaction.Hostile;

        // 4. Check explicit friend factions (player lists unit's faction as friend)
        if (IsFriendFaction(playerFt, unitFt.FactionId))
            return UnitReaction.Friendly;

        // 5. Check hostile mask (unit's hostile mask matches player's our mask)
        if ((unitFt.HostileMask & playerFt.OurMask) != 0)
            return UnitReaction.Hostile;

        // 6. Check friendly mask (unit's friendly mask matches player's our mask)
        if ((unitFt.FriendlyMask & playerFt.OurMask) != 0)
            return UnitReaction.Friendly;

        // 7. Check hostile mask (player's hostile mask matches unit's our mask)
        if ((playerFt.HostileMask & unitFt.OurMask) != 0)
            return UnitReaction.Hostile;

        // 8. Check friendly mask (player's friendly mask matches unit's our mask)
        if ((playerFt.FriendlyMask & unitFt.OurMask) != 0)
            return UnitReaction.Friendly;

        return UnitReaction.Neutral;
    }

    /// <summary>
    /// Quick hostility check — returns true if the unit is Hated, Hostile, or Unfriendly.
    /// </summary>
    public static bool IsHostile(uint playerFactionTemplateId, uint unitFactionTemplateId)
    {
        var reaction = GetReaction(playerFactionTemplateId, unitFactionTemplateId);
        return reaction <= UnitReaction.Unfriendly;
    }

    /// <summary>
    /// Quick friendliness check — returns true if the unit is Friendly or better.
    /// </summary>
    public static bool IsFriendly(uint playerFactionTemplateId, uint unitFactionTemplateId)
    {
        return GetReaction(playerFactionTemplateId, unitFactionTemplateId) >= UnitReaction.Friendly;
    }

    private static bool IsEnemyFaction(FactionTemplate ft, uint factionId)
    {
        if (factionId == 0) return false;
        return ft.EnemyFaction1 == factionId || ft.EnemyFaction2 == factionId ||
               ft.EnemyFaction3 == factionId || ft.EnemyFaction4 == factionId;
    }

    private static bool IsFriendFaction(FactionTemplate ft, uint factionId)
    {
        if (factionId == 0) return false;
        return ft.FriendFaction1 == factionId || ft.FriendFaction2 == factionId ||
               ft.FriendFaction3 == factionId || ft.FriendFaction4 == factionId;
    }

